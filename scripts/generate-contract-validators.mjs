import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

import Ajv2020 from "ajv/dist/2020.js";
import standaloneCode from "ajv/dist/standalone/index.js";
import addFormats from "ajv-formats";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const schemaPath = path.join(
  root,
  "packages/contracts/schema/solera.schema.json",
);
const outputPath = path.join(
  root,
  "packages/contracts/src/generated/validators.js",
);
const schema = JSON.parse(fs.readFileSync(schemaPath, "utf8"));
const ajv = new Ajv2020({
  allErrors: true,
  strict: true,
  allowUnionTypes: true,
  code: {
    source: true,
    esm: true,
    optimize: true,
  },
});
addFormats(ajv);
ajv.addSchema(schema);

const exports = {};
for (const definition of ["PageContext", "ViewSpec"]) {
  const id = `${schema.$id}/${definition}`;
  ajv.addSchema(
    {
      $id: id,
      $ref: `${schema.$id}#/$defs/${definition}`,
    },
    id,
  );
  exports[`validate${definition}Schema`] = id;
}

const generated = standaloneCode(ajv, exports);
fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(
  outputPath,
  `// Generated by scripts/generate-contract-validators.mjs. Do not edit.\n${generated}`,
);
console.log(outputPath);
